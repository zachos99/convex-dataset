import re, unicodedata
import os
import pandas as pd

"""
    Decision rules:
        - Split the note into sentences.
        - For each class, count how many patterns match across sentences.
        - If a pattern and its negation appear in the same sentence, ignore that hit.
        - Pick the class with the highest count.
        - If the top count is 0 → other.
        - If there's a tie, use priority: AI > Edited > Miscaptioned.
"""

PATTERNS = {
    "miscaptioned": [
        # general context / miscaption
        r"(?<!\w)out[- ]of[- ]context(?!\w)",
        r"\bwrong context\b",
        r"\bmiscaptioned\b",
        r"\bmisleading\b",
        r"\b(old|previous) (photo|video)\b",
        r"\b(reused|recycled) (photo|video)\b",
        r"\bnot from (this|that) (event|day|place|country)\b",
        r"\b(?:actually|originally|first posted|circulating since)\b.{0,12}\b(19[6-9]\d|20\d\d)\b(?!\s?(?:p|px|x)\d{3,4})",
        r"\b(?:photo|image|video|footage)\b.{0,14}\b(?:from|taken|filmed|recorded|shot)\b.{0,8}\b(19[6-9]\d|20\d\d)\b(?!\s?(?:p|px|x)\d{3,4})",
    ],

    "edited": [
        # digital manipulation keywords
        r"(?<!\w)photoshop(ped)?(?!\w)",
        r"\b(doctored|manipulated|tampered|fabricated|edited)\b",
        r"\b(digitally )?altered\b",
        r"\b(composite|spliced|superimposed|overlay)\b",
        r"\b(object|person) (added|removed)\b",
        r"\b(face|head)\s*swap\b",
        r"\bstitched\b",
        r"\bmerged (clip|video)\b",
        r"\bvideo edit(ed)?\b",
        r"\b(cropped|airbrushed|retouched)\b",
    ],

    "ai_generated": [
        # synthetic / AI-made content 
        r"\bai[- ]?generated\b", r"\bai[- ]?created\b",
        r"\bsynthetic (image|video)\b",
        r"\bgenerated by ai\b", r"\bcreated with ai\b",
        r"\b(diffusion model|gan)\b",
        r"\bmidjourney\b", r"\bdall[ -]?e\b",
        r"\brunway (gen[- ]?2|gen[- ]?3)?\b", r"\bgen[- ]?3\b", # “Gen 3” or “Gen-3”,
        r"\bsora\b", r"\bveo\b",
        r"\bmachine generated\b", r"\blooks (ai|synthetic)\b",
        r"\bfake (image|video)\b", r"\bdeepfake\b",
    ]
}


NEGATIONS = {
  "ai_generated": [
    r"\bnot ai[- ]?generated\b",
    r"\bnot (created|made) (by|with) ai\b",
    r"\b(real|authentic|genuine) (image|video|photo|footage)\b",
  ],
  "edited": [
    r"\bnot (photoshopped|edited|manipulated|doctored|altered)\b",
    r"\b(unaltered|unedited|original) (image|video|photo|footage)\b",
  ],
  "miscaptioned": [
    r"\bcaption (is )?accurate\b",
    r"\bcontext (is )?correct\b",
  ],
}


PRIORITY = ("ai_generated", "edited", "miscaptioned") 


def _normalize(text):
    if not isinstance(text, str):
        return ""
    t = unicodedata.normalize("NFKC", text)
    t = t.casefold()
    t = re.sub(r"\s+", " ", t).strip()
    return t


def split_sentences(text):
    # return re.split(r'(?<=[.!?])\s+', text.strip())
    return re.split(r'(?<=[.!?…])\s+|[\n;]+', text.strip())



def count_hits_in_sentence(sent, cls, compiled_patterns, compiled_negs):
    """
    Return total hits for class `cls` in `sent`, canceled to 0 if a negation for that class is present in the same sentence. Counts ALL occurrences (not just distinct patterns)
    """
    # If any negation for this class appears in the same sentence, cancel the hits
    if any(p.search(sent) for p in compiled_negs.get(cls, [])):
        return 0, {}  # zero hits, no keyword increments

    total = 0
    per_pattern = {}
    for pat_str, pat_rx in compiled_patterns[cls]:
        hits = len(pat_rx.findall(sent))
        if hits > 0:
            total += hits
            per_pattern[pat_str] = hits
    return total, per_pattern


def decide_label_from_counts(counts):
    """
    Minimal decision rule:
      - pick class with highest count
      - if top count == 0 -> 'other'
      - if tie -> use priority: AI > Edited > Miscaptioned
    """
    top_lbl, top_ct = max(counts.items(), key=lambda kv: kv[1])
    if top_ct == 0:
        return "other"
    tied = [lbl for lbl, ct in counts.items() if ct == top_ct]
    if len(tied) > 1:
        for p in PRIORITY:
            if p in tied:
                return p
    return top_lbl


def classify_note(note_text, compiled_patterns, compiled_negs):
    """
    Classify a single noteText string.
    Returns (label, class_counts, per_keyword_counts)
      - class_counts: total hits per class
      - per_keyword_counts: dict {pattern_string: hits_in_this_note}
    """
    if not isinstance(note_text, str) or not note_text.strip():
        return "other", {"ai_generated": 0, "edited": 0, "miscaptioned": 0}, {}

    per_class = {k: 0 for k in PATTERNS.keys()}
    per_keyword = {}  # pattern string -> total hits in this note

    note_text = _normalize(note_text)
    
    sentences = split_sentences(note_text)

    for sent in sentences:
        s = sent.strip()
        if not s:
            continue
        for cls in PATTERNS.keys():
            hits, pat_hits = count_hits_in_sentence(s, cls, compiled_patterns, compiled_negs)
            per_class[cls] += hits
            for pat_str, h in pat_hits.items():
                per_keyword[pat_str] = per_keyword.get(pat_str, 0) + h

    label = decide_label_from_counts(per_class)
    return label, per_class, per_keyword




def process_file(input_path, output_path=None, start=None, end=None):

    # Precompile regex (speed + correctness)
    compiled_patterns = {
        cls: [(pat, re.compile(pat, flags=re.IGNORECASE)) for pat in pats]
        for cls, pats in PATTERNS.items()
    }
    compiled_negs = {
        cls: [re.compile(pat, flags=re.IGNORECASE) for pat in pats]
        for cls, pats in NEGATIONS.items()
    }

    df = pd.read_csv(input_path)
    if start is not None and end is not None:
        df = df.iloc[start:end].copy()

    # initialize global counters
    global_keyword_counts = {pat: 0 for pats in PATTERNS.values() for pat in pats}
    class_counts = {"ai_generated": 0, "edited": 0, "miscaptioned": 0, "other": 0}

    labels = []
    for txt in df["noteText"].fillna(""):
        label, _, per_kw = classify_note(txt, compiled_patterns, compiled_negs)
        labels.append(label)
        class_counts[label] = class_counts.get(label, 0) + 1
        for pat, c in per_kw.items():
            global_keyword_counts[pat] += c

    df["misinfo_type_keys"] = labels

    if output_path is None:
        base, ext = os.path.splitext(input_path)
        output_path = f"{base}_with_misinfo_keys{ext}"
    df.to_csv(output_path, index=False)

    # --- Print summaries ---
    print("\n=== Keyword occurrence counts (total across file) ===")
    for pat, c in sorted(global_keyword_counts.items(), key=lambda kv: (-kv[1], kv[0])):
        if c > 0:
            print(f"{c:6d}  {pat}")

    print("\n=== Classified note counts ===")
    total_notes = sum(class_counts.values())
    for cls, c in class_counts.items():
        pct = (c / total_notes * 100) if total_notes > 0 else 0
        print(f"{cls:15s}: {c:6d}  ({pct:5.2f}%)")
    print(f"\nTotal notes processed: {total_notes:,}")


    print(f"\nSaved with labels -> {output_path}")
    return output_path, global_keyword_counts, class_counts





